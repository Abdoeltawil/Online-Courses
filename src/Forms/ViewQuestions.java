    public ViewQuestions(User user) {
        initComponents();
        this.user = user;
        File file = new File("StudentQuestions.txt");
        Scanner scan;
        String s = "";
        try {
           scan = new Scanner(file);
        while(scan.hasNext())
        {
            s+=scan.nextLine(); 
            s+="\n";
        }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(log_in.class.getName()).log(Level.SEVERE, null, ex);
        }
        questions = s.split("/");
        TraverseQuestions();
    }

    private void TraverseQuestions()
    {
        if(questionNumber == questions.length - 1)
        {
            JOptionPane.showMessageDialog(this,"These were all the questions you have","Alert",JOptionPane.INFORMATION_MESSAGE);
          
        
            Teacher_Main t = new Teacher_Main(user);
            t.setVisible(true);
            this.hide();
        
        }
        String currentQuestion[] = questions[questionNumber].split("#");
        
        jTextArea1.setText(currentQuestion[0]);
    }
