    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
User user = new Student() ;
        String name = null,username = null,pass = null;
        if (!exception()){
             JOptionPane.showMessageDialog(this,"There's an empty field","Alert",JOptionPane.WARNING_MESSAGE);}
        java.io.File file = new java.io.File("Data.txt");
        Scanner scanner;
        String all = null ;
        boolean found = false ;
       

        try {
            scanner = new Scanner(file);
            all = scanner.next();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(log_in.class.getName()).log(Level.SEVERE, null, ex);
        }
        String users[]=all.split("/");
        for(int i=0;i<users.length;++i)
        {
            String temp[] = users[i].split("%");
          
            if(temp[1].equals(jTextField1.getText())&&temp[2].equals(jPasswordField1.getText()))
            {
                name=temp[0];
                username=temp[1];
                pass=temp[2];
                found = true ;
                break;
            }
        }
        if(!found)
        {
            JOptionPane.showMessageDialog(this,"Your account or password doen't exist","Alert",JOptionPane.WARNING_MESSAGE);
            return ;
        }
        
        
        if (exception()){
        JOptionPane.showMessageDialog(this,"Welcome","Alert",JOptionPane.INFORMATION_MESSAGE);
//       user.getcurrentuser(jTextField1.getText(),jPasswordField1.getText());
      
        }

        if(jRadioButton1.isSelected())
        {
            user = new Teacher();
            user.setName(name);
            user.setPassword(pass);
            user.setUser_name(username);
            Teacher_Main t = new Teacher_Main(user);
            
            t.setVisible(true);
            System.out.println("Success");
            this.hide();

            return;

        }
        else if (jRadioButton2.isSelected())
        {
            user = new Student();
            user.setName(name);
            user.setPassword(pass);
            user.setUser_name(username);
            Student_main t = new Student_main(user);
            t.setVisible(true);
            System.out.println("Success");
            this.hide();

            return;
        }
      

    }//GEN-LAST:event_jButton1ActionPerformed

